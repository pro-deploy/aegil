"""Неизменяемый журнал аудита операций панели kube-sentinel. Append-only, одна строка это один
канонический объект формата JSON (тот же канон, что и остальные логи продукта), поэтому действия
оператора и автономного агента сами становятся объектом наблюдаемости и попадают через Alloy в
Loki. Аудит фиксирует три рода операций над кластером:

  1. чтение (observe): агент или оператор читает состояние кластера, логи, метрики. Раньше
     чтения не протоколировались вовсе, хотя обращение к состоянию тоже след; теперь примитив
     умеет писать записи чтения через op_type="read" (результат чтения, актор, цель, argv);
  2. постановку команды в отложенное подтверждение (pending): мутирующая команда, требующая
     подтверждения оператора, ставится в очередь и в этот момент аудируется с результатом
     "pending", чтобы факт заявки был виден до самого исполнения;
  3. исполнение мутирующей команды (execute): фактический запуск с результатом исполнения.

Формат записи отражает реальную операцию продукта: тип операции (op_type), класс опасности
команды по детерминированному классификатору (danger_class: read, safe_write, destructive),
актор (actor: имя оператора либо "agent" для автономного цикла), цель (target), список аргументов
команды (argv) и текстовый результат. Наследных полей исходной платформы (service adminchat,
event admin.action, ссылок на нормативные акты о персональных данных и биометрии) в записи нет.

Каталог журнала выносится за пределы рабочего дерева агента на постоянный том (по умолчанию
каталог данных SENTINEL_STATE_DIR, обычно /data; конкретный файл переопределяется переменной
SENTINEL_AUDIT). Это принципиально: если бы журнал лежал внутри репозитория, детерминированный
классификатор отнёс бы его к безопасно удаляемым и агент стёр бы собственный аудит как обычную
чистку временных путей.

Долговечная запись уходит двумя каналами. Основной канал это стандартный вывод процесса:
канонической строкой его забирает агент сбора логов в централизованное хранилище, и это надёжный
неизменяемый след, не зависящий от файловой системы пода. Дополнительный канал это локальный файл
на постоянном томе. Сбой файловой записи НЕ глотается молча: ошибка сигналится в стандартный поток
ошибок, потому что аудит есть обязательное условие исполнения привилегированной команды. При этом
запрос не роняется, так как долговечная запись уже ушла в стандартный вывод.
"""
from __future__ import annotations

import json
import os
import sys
import threading
import time
from pathlib import Path

# Классы опасности команд (совпадают с классификатором политики): только чтение, обратимый
# ремонт, необратимое действие.
CLASS_READ = "read"
CLASS_SAFE_WRITE = "safe_write"
CLASS_DESTRUCTIVE = "destructive"

# Типы операций аудита.
OP_READ = "read"        # наблюдение, обращение к состоянию кластера
OP_PENDING = "pending"  # постановка мутирующей команды в отложенное подтверждение
OP_EXECUTE = "execute"  # фактическое исполнение команды

# Актор автономного агентного цикла (когда операцию совершает не человек-оператор, а агент).
ACTOR_AGENT = "agent"

# Единая блокировка записи: обработчики FastAPI и такт автопилота пишут аудит из разных потоков,
# и без блокировки строки в файле перемешивались бы.
_LOCK = threading.Lock()


def _state_dir() -> Path:
    """Каталог данных вне рабочего дерева. По умолчанию /data (постоянный том), переопределяется
    переменной SENTINEL_STATE_DIR."""
    return Path(os.getenv("SENTINEL_STATE_DIR", "/data"))


def _default_audit_path() -> Path:
    """Путь журнала аудита. Явный SENTINEL_AUDIT имеет приоритет, иначе файл в каталоге данных.
    Ни один вариант не лежит внутри рабочего дерева агента, поэтому агент не может отнести журнал
    к безопасно удаляемым и стереть собственный аудит."""
    explicit = os.getenv("SENTINEL_AUDIT")
    if explicit:
        return Path(explicit)
    return _state_dir() / "audit.log.jsonl"


AUDIT_PATH = _default_audit_path()


def _now() -> str:
    t = time.time()
    return time.strftime("%Y-%m-%dT%H:%M:%S", time.gmtime(t)) + ".%03dZ" % int((t % 1) * 1000)


def audit_write(actor: str, command: str, params: dict, target: str,
                confirmed: bool, result: str, trace_id: str = "",
                op_type: str = OP_EXECUTE, danger_class: str = "",
                argv: list | None = None) -> dict:
    """Пишет запись аудита и возвращает её.

    Первые семь параметров сохранены в прежнем порядке ради совместимости с существующими
    вызовами исполнителя команд и автопилота: actor (имя оператора либо "agent"), command
    (человекочитаемое имя операции), params (структурированные параметры операции), target
    (цель операции: инцидент, сервис, узел, путь), confirmed (было ли подтверждение оператора),
    result (текстовый результат), trace_id (идентификатор трассировки).

    Расширенные параметры описывают операцию точнее: op_type относит запись к чтению (OP_READ),
    постановке в отложенное подтверждение (OP_PENDING) либо исполнению (OP_EXECUTE);
    danger_class фиксирует класс опасности по детерминированному классификатору (CLASS_READ,
    CLASS_SAFE_WRITE, CLASS_DESTRUCTIVE); argv несёт список аргументов команды, если операция
    сводится к запуску процесса. Пустые расширенные поля в запись не попадают.

    Ошибка файловой записи НЕ глотается: она сигналится в стандартный поток ошибок. Запрос при
    этом не роняется, потому что долговечная копия уже ушла в стандартный вывод (её заберёт агент
    сбора логов в централизованное хранилище)."""
    rec: dict = {
        "ts": _now(),
        "op_type": op_type,
        "actor": actor,
        "command": command,
        "params": params,
        "target": target,
        "confirmed": confirmed,
        "result": result,
    }
    if danger_class:
        rec["danger_class"] = danger_class
    if argv is not None:
        rec["argv"] = list(argv)
    if trace_id:
        rec["trace_id"] = trace_id
    line = json.dumps(rec, ensure_ascii=False)
    # Основной долговечный канал: стандартный вывод. Канонической строкой его заберёт агент сбора
    # логов в централизованное хранилище. Это надёжный неизменяемый след, не зависящий от
    # файловой системы пода.
    print(line, flush=True)
    # Дополнительный канал: локальный файл на постоянном томе. Пишется под блокировкой, чтобы
    # конкурентные записи из потока автопилота и обработчиков FastAPI не перемешивались.
    global AUDIT_PATH
    try:
        with _LOCK:
            AUDIT_PATH.parent.mkdir(parents=True, exist_ok=True)
            with AUDIT_PATH.open("a", encoding="utf-8") as f:
                f.write(line + "\n")
    except OSError as exc:
        # Сбой файловой записи не роняет запрос (долговечная копия уже в стандартном выводе), но и
        # не глотается молча: сигналим в стандартный поток ошибок, чтобы деградация локального
        # журнала была видна в наблюдаемости.
        print(f"audit: локальная запись в {AUDIT_PATH} не удалась: {exc}",
              file=sys.stderr, flush=True)
    return rec


def audit_read(actor: str, command: str, target: str, result: str,
               params: dict | None = None, argv: list | None = None,
               trace_id: str = "") -> dict:
    """Аудит операции чтения (наблюдения): обращение к состоянию кластера, логам, метрикам. Класс
    опасности всегда read, подтверждение не требуется. Удобная обёртка над audit_write для
    единообразной фиксации чтений, которые раньше не протоколировались."""
    return audit_write(actor, command, params or {}, target, confirmed=False,
                       result=result, trace_id=trace_id, op_type=OP_READ,
                       danger_class=CLASS_READ, argv=argv)


def audit_pending(actor: str, command: str, params: dict, target: str,
                  danger_class: str, argv: list | None = None,
                  trace_id: str = "") -> dict:
    """Аудит постановки мутирующей команды в отложенное подтверждение оператора. Фиксирует факт
    заявки до самого исполнения: результат "pending", подтверждение ещё не получено. Удобная
    обёртка над audit_write."""
    return audit_write(actor, command, params, target, confirmed=False,
                       result="pending", trace_id=trace_id, op_type=OP_PENDING,
                       danger_class=danger_class, argv=argv)
