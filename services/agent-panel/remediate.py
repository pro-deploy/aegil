"""Детерминированный выбор ремонтного действия по инциденту, без языковой модели.

Автономный ремонт не должен зависеть от способности модели вызывать инструменты: модель
может её не иметь (например gemma без парсера инструментов в vLLM) либо ошибаться, и тогда
весь автономный контур перестаёт чинить. Поэтому для хорошо понятных симптомов Kubernetes
ремонтное действие выбирается детерминированной таблицей соответствия симптома действию, а
исполняется через тот же детерминированный гейт опасности, гарды и аудит, что и любое
автономное действие. Языковая модель остаётся для объяснения оператору, а не для выбора
действия, и физически не может обойти гейт.

Действие предлагается только для симптомов, которые обратимо лечатся перезапуском
безсостоятельного сервиса: циклическое падение контейнера, убийство по нехватке памяти,
рестарт-шторм, недоступность деплоймента. Симптомы, которые перезапуском НЕ лечатся (битый
образ, переполнение диска, отказ узла), намеренно не предлагают автономного действия: их
место у оператора, а не у слепого перезапуска, который лишь замаскирует причину.
"""
from __future__ import annotations

# Симптомы, обратимо лечимые перезапуском безсостоятельного сервиса. Коды совпадают с
# кодами алертов (модуль alerts).
RESTARTABLE = {"crashloop", "oom", "restart_storm", "deploy_unavailable"}


def propose(alert: dict) -> dict | None:
    """По алерту возвращает детерминированное ремонтное действие вида
    {argv, target, service, why, params} либо None, если симптом не лечится автономным
    перезапуском. Само исполнение и проверка гейтом происходят выше, в agent_exec."""
    code = str(alert.get("code") or "")
    params = alert.get("params") or (alert.get("verdict") or {}).get("params") or {}
    service = str(params.get("service") or "").strip()
    if code in RESTARTABLE and service:
        return {
            "argv": ["kubectl", "rollout", "restart", service],
            "target": "cluster",
            "service": service,
            "why": f"детерминированный ремонт симптома «{code}» перезапуском сервиса {service}",
            "params": {"service": service, "code": code},
        }
    return None
