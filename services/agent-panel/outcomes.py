"""Замыкание контура активного обучения на исходах ремонтов kube-sentinel.

Тонкий модуль, вызываемый панелью в точках разрешения инцидента (автоматическое устранение,
подтверждённое проверкой, либо разрешение оператором). Каждый разрешённый инцидент превращается
в размеченный пример: отпечаток симптома, статус вердикта, диагностированная первопричина,
применённое действие ремонта и признак фактического устранения. Накопленные исходы позже питают
дообучение, поэтому потеря записи нежелательна, но и не должна ронять разрешение инцидента.

Каждый исход оставляет два независимых следа, чтобы отказ одного канала не терял пример целиком.
Первый след долговечный и локальный: строка формата JSON дозаписывается в файл outcomes.jsonl под
каталогом состояния вне рабочего дерева (переменная SENTINEL_STATE_DIR, по умолчанию /data).
Запись выполняется под общей блокировкой, чтобы конкурентные записи из потока автопилота и
обработчиков веб-слоя не перемешивали строки, и атомарно построчно. При недоступности каталога
состояния (том не смонтирован, нет прав) запись откатывается на временный каталог, а деградация
сигналится в стандартный поток ошибок, а не гасится молча. Второй след это отправка исхода в
сервис разбора первопричин через клиент rca_client, выполняемая best-effort: её отказ не роняет
разрешение инцидента и не влияет на локальный журнал.

Адрес сервиса разбора первопричин по умолчанию берётся из конфигурации продукта (config.RCA_URL),
поэтому продукт не привязан к конкретному развёртыванию, а конкретный вызов может задать адрес
явно.
"""
from __future__ import annotations

import json
import os
import sys
import tempfile
import threading
import time
from pathlib import Path

import config
import rca_client

# Единая блокировка записи: разрешение инцидента может прийти как из такта автопилота, так и из
# обработчика веб-слоя, из разных потоков, поэтому дозапись строки в журнал сериализуется, чтобы
# строки не перемешивались.
_LOCK = threading.Lock()

# Имя файла локального журнала исходов внутри каталога состояния.
_JOURNAL_NAME = "outcomes.jsonl"


def _state_dir() -> Path:
    """Каталог данных вне рабочего дерева. По умолчанию /data (постоянный том), переопределяется
    переменной SENTINEL_STATE_DIR. Тот же каталог, что использует аудит и журнал инцидентов, чтобы
    все долговечные следы лежали на одном постоянном томе, а не внутри рабочего дерева агента."""
    return Path(os.getenv("SENTINEL_STATE_DIR", "/data"))


def journal_path() -> Path:
    """Путь локального журнала исходов ремонтов. Вычисляется при каждом вызове, чтобы
    переопределение SENTINEL_STATE_DIR (в частности, в модульных тестах) вступало в силу без
    перезагрузки модуля."""
    return _state_dir() / _JOURNAL_NAME


def _fingerprint(verdict: dict) -> str:
    """Отпечаток симптома из вердикта. Готовое поле fingerprint имеет приоритет; при его отсутствии
    отпечаток вычисляется тем же способом, что и в модуле инцидентов (статус, набор детекторов,
    первопричина), чтобы исход сходился по отпечатку с открытым инцидентом."""
    fp = verdict.get("fingerprint")
    if fp:
        return str(fp)
    status = str(verdict.get("status", ""))
    dets = ",".join(sorted(verdict.get("detectors") or []))
    rc = str(verdict.get("root_cause") or "")
    return f"{status}|{dets}|{rc}"


def _build_payload(verdict: dict, action: str, resolved: bool) -> dict:
    """Строит тело исхода ремонта из вердикта и параметров разрешения инцидента."""
    return {
        "fingerprint": _fingerprint(verdict),
        "status": verdict.get("status"),
        "root_cause": verdict.get("root_cause"),
        "action": action,
        "resolved": bool(resolved),
    }


def _now() -> str:
    """Метка времени формата ISO 8601 с миллисекундами в зоне UTC."""
    t = time.time()
    return time.strftime("%Y-%m-%dT%H:%M:%S", time.gmtime(t)) + ".%03dZ" % int((t % 1) * 1000)


def _append_line(line: str) -> None:
    """Дозаписывает строку журнала под блокировкой. При недоступности основного каталога состояния
    запись откатывается на временный каталог, а деградация сигналится в стандартный поток ошибок,
    а не гасится молча."""
    with _LOCK:
        path = journal_path()
        try:
            path.parent.mkdir(parents=True, exist_ok=True)
            with path.open("a", encoding="utf-8") as f:
                f.write(line + "\n")
            return
        except OSError as exc:
            fallback = Path(tempfile.gettempdir()) / _JOURNAL_NAME
            print(f"outcomes: локальная запись в {path} не удалась ({exc}); "
                  f"откат на {fallback}", file=sys.stderr, flush=True)
            try:
                with fallback.open("a", encoding="utf-8") as f:
                    f.write(line + "\n")
            except OSError as exc2:
                # Отказал и запасной каталог: сигналим и сдаёмся, не роняя разрешение инцидента,
                # потому что второй след (push в сервис разбора первопричин) остаётся.
                print(f"outcomes: запасная запись в {fallback} не удалась ({exc2})",
                      file=sys.stderr, flush=True)


def record(verdict: dict, action: str, resolved: bool, *, rca_url: str | None = None) -> None:
    """Фиксирует исход ремонта инцидента, оставляя два независимых следа.

    Первый след долговечный и локальный: строка формата JSON дозаписывается в файл outcomes.jsonl
    под каталогом состояния (потокобезопасно и атомарно построчно, с откатом на временный каталог
    при недоступности основного). Второй след это отправка исхода в сервис разбора первопричин
    через rca_client.record_outcome, выполняемая best-effort: её отказ не роняет вызывающего.

    Параметр verdict несёт вердикт разрешённого инцидента (из него берутся отпечаток симптома,
    статус и первопричина), action это применённое действие ремонта, resolved признак фактического
    устранения. Адрес сервиса разбора первопричин по умолчанию берётся из config.RCA_URL и может
    быть задан явно параметром rca_url."""
    payload = _build_payload(verdict, action, resolved)
    record_at = rca_url or config.RCA_URL

    # Первый след: долговечная локальная строка журнала. Метка времени и адрес назначения
    # добавляются к телу исхода, чтобы локальная запись была самодостаточной для разбора.
    entry = dict(payload)
    entry["ts"] = _now()
    _append_line(json.dumps(entry, ensure_ascii=False))

    # Второй след: push в сервис разбора первопричин, best-effort. Клиент сам мягко деградирует и
    # возвращает False при любом сбое, поэтому здесь исключений не ожидается; на всякий случай
    # оборачиваем, чтобы никакой сбой второго канала не отменил уже записанный первый след.
    try:
        rca_client.record_outcome(record_at, payload)
    except Exception as exc:  # защита от неожиданного сбоя клиента
        print(f"outcomes: push исхода в {record_at} не удался ({exc})",
              file=sys.stderr, flush=True)
