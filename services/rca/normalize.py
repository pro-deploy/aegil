"""Нормализация лог-сообщения в шаблон (ADR-0032, Часть B.1; книга Биркина, глава 3,
идея Drain3 без внешней зависимости). Переменные части (идентификаторы, адреса,
числа) маскируются, чтобы схожие события сворачивались в один шаблон и агрегатор
считал доминирующие паттерны и их долю. Персональные данные (почта, телефоны)
маскируются раньше, на приёме в Loki (A.3), поэтому здесь не дублируются.

Порядок масок важен: сначала более специфичные (uuid, ip, длинный hex), затем
общие числа, иначе число внутри адреса схлопнется преждевременно.
"""
from __future__ import annotations

import re

_UUID = re.compile(r"\b[0-9a-fA-F]{8}-[0-9a-fA-F]{4}-[0-9a-fA-F]{4}-[0-9a-fA-F]{4}-[0-9a-fA-F]{12}\b")
_IP = re.compile(r"\b\d{1,3}(?:\.\d{1,3}){3}\b")
_HEX = re.compile(r"\b[0-9a-fA-F]{16,}\b")
_NUM = re.compile(r"\b\d+")  # без замыкающей границы, чтобы ловить «30s» -> «<num>s»


def template(msg: str) -> str:
    """Возвращает шаблон сообщения с замаскированными переменными частями."""
    if not msg:
        return ""
    s = _UUID.sub("<uuid>", msg)
    s = _IP.sub("<ip>", s)
    s = _HEX.sub("<hex>", s)
    s = _NUM.sub("<num>", s)
    return s
